let currentLang = 'es';

function setLang(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (translations[lang][key] !== undefined) {
      el.textContent = translations[lang][key];
    }
  });

  // Update toggle buttons that track open/closed state separately
  document.querySelectorAll('.lote-toggle').forEach(btn => {
    const isOpen = btn.getAttribute('aria-expanded') === 'true';
    const key = isOpen ? btn.dataset.keyClose : btn.dataset.keyOpen;
    btn.textContent = translations[lang][key];
  });

  const langBtn = document.getElementById('lang-btn');
  langBtn.textContent = lang === 'es' ? 'EN' : 'ES';
}

document.getElementById('lang-btn').addEventListener('click', () => {
  setLang(currentLang === 'es' ? 'en' : 'es');
});
